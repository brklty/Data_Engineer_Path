#########################################################################

# Virtual Environment and Package Management

#############################################################################

# Listing of virtual environments:
# conda env list

# Creating a virtual environment:
# conda create -n myenv

# Activating the virtual environment:
# conda activate myenv

# Deactivating the virtual environment:
# conda deactivate

# Delete virtual environment:
# conda env remove -n sanal_ortam_Adi

# Create a new virtual environment with environment.yaml
# conda env create -f environment.yaml

# Listing of installed packages:
# conda list

# Package installation:
# conda install numpy

# Installing more than one package at the same time:
# conda install numpy scipy pandas

# Delete package:
# conda remove package_name

# Package installation according to a specific version:
# conda install numpy=1.20.1  (pip'te == oluyor)

# Package Upgrade:
# conda upgrade numpy

# Upgrade of all packages:
# conda upgrade -all

# pip: pypi (python package index) package management tool

# Package installation:
# pip install paket_adi

# Package installation according to a specific version:
# pip install pandas==1.2.1

